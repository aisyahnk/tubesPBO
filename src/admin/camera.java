            String sql = "INSERT INTO camera VALUES " + "(" + "'"+id.getText()+ "'," + "'"+nama.getText()+ "'," + "'"+jenis.getText()+"'," + "'"+merek.getText()+"',"+ "'"+lensa.getText()+"',"+ "'"+harga.getText()+"',"+ "'"+keterangan.getText()+"'"+ ")";

