     private void tampilkeunn() {
      int row = tabel_camera.getRowCount();
        for (int a = 0; a < row; a++) {
        model.removeRow(0);
        }
        
        try {
            conngui = Koneksi.getKoneksi();
            ResultSet rs = conngui.createStatement().executeQuery("SELECT * FROM camera");
            
            while(rs.next()) {
                String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7)};
                model.addRow(data);
            }
                
        } catch (Exception e) {
            
        }
    }
     
     private void cariData(String key){
        try{

            String [] judul = {"No","Nama", "Jenis", "Merek", "Lensa", "Harga", "Keterangan"  };
            model = new DefaultTableModel(null, judul);
            tabel_camera.setModel(model);
            
            conngui = Koneksi.getKoneksi();
            Statement stt=conngui.createStatement();
            model.getDataVector().removeAllElements();
            
            ResultSet RsProduk = stt.executeQuery("SELECT * from camera WHERE id LIKE '%"+key+"%' OR nama LIKE '%"+key+"%'");  
            while(RsProduk.next()){
                Object[] data={
                    RsProduk.getString("id"),
                    RsProduk.getString("Nama"),
                    RsProduk.getString("Jenis"),
                    RsProduk.getString("Merek"),
                    RsProduk.getString("Lensa"),
                    RsProduk.getString("Harga"),
                    RsProduk.getString("Keterangan")
                };
               model.addRow(data);
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }
    }
     
